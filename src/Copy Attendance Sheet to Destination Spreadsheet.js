function newAttendanceTemplate(sourceUrl) {
  var source = SpreadsheetApp.getActiveSpreadsheet();

  var sheet = source.getSheetByName('Attendance Template');

  var destination = SpreadsheetApp.openByUrl(sourceUrl);
  sheet.copyTo(destination);

  //Find current attendance template and delete
  var template = destination.getSheetByName('Attendance Template');
  destination.setActiveSheet(template);
  destination.deleteActiveSheet();

  //Move New Attendance Template to sheet position 3
  var totalSheets = destination.getNumSheets();
  var lastSheet = destination.getSheets()[totalSheets-1];
  destination.setActiveSheet(lastSheet);
  destination.moveActiveSheet(3);

  //Change name of template copy to Attendance Template
  destination.renameActiveSheet('Attendance Template');


  //Protect Attendance Template so that only the owner (me) can edit sheet
  var sheetToProtect = destination.getSheetByName('Attendance Template');
  sheetToProtect.protect();
  var protection = sheetToProtect.protect().setDescription('Attendance Template is uneditable');  
  protection.removeEditors(protection.getEditors());
  if (protection.canDomainEdit()) {
    protection.setDomainEdit(false);
  }
}
